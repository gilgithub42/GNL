#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int				i;
	unsigned char	car1;
	unsigned char	car2;

	i = 0;
	while ((s1[i] == s2[i]) && (s1[i] != '\0') && (s2[i] != '\0'))
		i++;
	car1 = s1[i];
	car2 = s2[i];
	return (car1 - car2);
}
